// Open blog detail modal
function openBlogDetail(blogId) {
  const blog = blogData[blogId];
  if (!blog) return;

  document.getElementById('modalTitle').textContent = blog.title;
  document.getElementById('modalMeta').innerHTML = `
    <span><i class="far fa-calendar"></i> ${blog.date}</span> • 
    <span><i class="far fa-user"></i> ${blog.author}</span>
  `;
  document.getElementById('modalContent').innerHTML = blog.content;

  document.getElementById('blogModal').style.display = 'block';
  document.body.style.overflow = 'hidden';
}

// Close modal
function closeBlogModal() {
  document.getElementById('blogModal').style.display = 'none';
  document.body.style.overflow = 'auto';
}

// Event listeners
document.addEventListener('DOMContentLoaded', function () {
  const modal = document.getElementById('blogModal');
  const closeBtn = document.querySelector('.blog-close');

  if (closeBtn) {
    closeBtn.onclick = closeBlogModal;
  }

  if (modal) {
    window.onclick = function (event) {
      if (event.target === modal) {
        closeBlogModal();
      }
    };
  }

  // Close on ESC key
  document.addEventListener('keydown', function (event) {
    if (event.key === 'Escape') {
      closeBlogModal();
    }
  });
});

// Make function available globally
window.openBlogDetail = openBlogDetail;
